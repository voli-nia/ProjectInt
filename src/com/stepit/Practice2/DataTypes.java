package com.stepit.Practice2;

public class DataTypes {
    public static void main(String[] args) {
        int myNum = 5;              // Integer (whole number)
        long myLong = 5L;           // Long (whole number)
        float myFloatNum = 5.99F;   //Floating point number
        double myDoubleNum = 5.99;  // Double point number
        boolean myBool = true;      // Boolean
        char myLetter = 'A';        // Character
        char anotherLetter = 65;    // Character
        char myAnotherLetter = '\u0041';    // Character


        System.out.println("myNum is " + myNum);
        System.out.println("myLong is " + myLong);
        System.out.println("myFloatNum is " + myFloatNum);
        System.out.println("myDoubleNum is " + myDoubleNum);
        System.out.println("myBool is " + myBool);
        System.out.println("myLetter is " + myLetter);
        System.out.println("anotherLetter is " + anotherLetter);
        System.out.println("myAnotherLetter is " + myAnotherLetter);
    }
}